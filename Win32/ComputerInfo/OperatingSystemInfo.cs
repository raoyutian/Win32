namespace Win32.ComputerInfo
{
    /// <summary>
    /// 操作系统
    /// </summary>
    public struct OperatingSystemInfo
    {
        /// <summary>
        /// 操作系统名称
        /// </summary>
        public string Caption;
        /// <summary>
        /// 操作系统版本号
        /// </summary>
        public string SerialNumber;
        /// <summary>
        /// 操作系统位数
        /// </summary>
        public string OSLevel;
        /// <summary>
        /// 系统安装时间
        /// </summary>
        public string InstallDate;
        /// <summary>
        /// 系统启动时间
        /// </summary>
        public string LastBootUpTime;
        /// <summary>
        /// 系统本地时间
        /// </summary>
        public string LocalDateTime;


    }
}
