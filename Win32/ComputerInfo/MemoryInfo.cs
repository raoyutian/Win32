namespace Win32.ComputerInfo
{
    /// <summary>
    /// 内存
    /// </summary>
    public struct MemoryInfo
    {
        /// <summary>
        /// 可用物理内存/M
        /// </summary>
        public ulong AvailablePhysicalMemory;
        /// <summary>
        /// 最大物理内存/M
        ///</summary>
        public ulong TotalPhysicalMemory;
        /// <summary>
        /// 可用虚拟内存/M
        /// </summary>
        public ulong AvailableVirtualMemory;
        /// <summary>
        /// 最大虚拟内存/M
        /// </summary>
        public ulong TotalVirtualMemory;
    }





}
